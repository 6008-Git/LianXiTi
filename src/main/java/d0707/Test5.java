public class Test5 {
    public static void main(String[] args) {
        int x = 8;
        x++;
        System.out.println(x);  //9
        System.out.println(x++);  //9
        System.out.println(++x);  //11
        System.out.println(x--);  //11
        System.out.println();
    }
}
